const ipv4Exp = new RegExp('^((1\\d{2}|2[0-4]\\d|25[0-5]|[1-9]?\\d)(\\.(?=\\d)|(?!\\d))){4}(\\\/([12]?\\d|3[0]))$', '')
const ipv6Exp = new RegExp('(?:(?:(?:[A-F0-9]{1,4}:){6}|(?=(?:[A-F0-9]{0,4}:){0,6}(?:[0-9]{1,3}\\.){3}[0-9]{1,3}(?![:.\\w]))(([0-9A-F]{1,4}:){0,5}|:)((:[0-9A-F]{1,4}){1,5}:|:)|::(?:[A-F0-9]{1,4}:){5})(?:(?:25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9]?[0-9])\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)|(?:[A-F0-9]{1,4}:){7}[A-F0-9]{1,4}|(?=(?:[A-F0-9]{0,4}:){0,7}[A-F0-9]{0,4}(?![:.\\w]))(([0-9A-F]{1,4}:){1,7}|:)((:[0-9A-F]{1,4}){1,7}|:)|(?:[A-F0-9]{1,4}:){7}:|:(:[A-F0-9]{1,4}){7})(?![:.\\w])\\\/(?:12[0-8]|1[01][0-9]|[1-9]?[0-9])', '')

export function isValidIpv4(string) {
    return ipv4Exp.test(string)
}

export function isValidIpv6(string) {
    return ipv6Exp.test(string)
}